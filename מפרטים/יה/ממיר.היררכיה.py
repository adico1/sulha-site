def write_hierarchy_to_sade():
    """כתוב היררכיה לשדה"""
    YESH_DIR.mkdir(exist_ok=True)
    sade_path = YESH_DIR / "שדה"

    # קרא שדה קיים
    existing = ""
    if sade_path.exists():
        with open(sade_path, 'r', encoding='utf-8') as f:
            existing = f.read()

    # הוסף היררכיה
    with open(sade_path, 'a', encoding='utf-8') as f:
        f.write("\n# היררכיה\n\n")

        # אחד
        f.write("אחד:\n")
        f.write(f"  שם: {HIERARCHY['אחד']['שם']}\n")
        f.write(f"  תפקיד: {HIERARCHY['אחד']['תפקיד']}\n\n")

        # שלשה
        f.write("שלשה:\n")
        f.write(f"  אבות: {', '.join(HIERARCHY['שלשה']['אבות'])}\n")
        f.write(f"  אמות: {' '.join(HIERARCHY['שלשה']['אמות'])}\n")
        f.write("  יסודות:\n")
        for elem, pos in HIERARCHY['שלשה']['יסודות'].items():
            f.write(f"    {elem}: {pos}\n")
        f.write(f"  עומד: {HIERARCHY['שלשה']['עומד']}\n\n")

        # שבעה
        f.write("שבעה:\n")
        f.write(f"  כפולות: {' '.join(HIERARCHY['שבעה']['כפולות'])}\n")
        f.write(f"  כוכבים: {', '.join(HIERARCHY['שבעה']['כוכבים'])}\n")
        f.write(f"  חלוקין: {HIERARCHY['שבעה']['חלוקין']}\n\n")

        # שנים עשר
        f.write("שנים_עשר:\n")
        f.write(f"  פשוטות: {' '.join(HIERARCHY['שנים_עשר']['פשוטות'])}\n")
        f.write(f"  אלכסונין: {HIERARCHY['שנים_עשר']['אלכסונין']}\n")
        f.write("  עומדין_במלחמה:\n")
        f.write(f"    אוהבים: {', '.join(HIERARCHY['שנים_עשר']['חלוקה']['אוהבים'])}\n")
        f.write(f"    שונאים: {', '.join(HIERARCHY['שנים_עשר']['חלוקה']['שונאים'])}\n")
        f.write(f"    מחיים: {HIERARCHY['שנים_עשר']['חלוקה']['מחיים']}\n")
        f.write(f"    ממיתים: {HIERARCHY['שנים_עשר']['חלוקה']['ממיתים']}\n\n")

        # פקידן
        f.write("פקידן:\n")
        for pkid, data in HIERARCHY['פקידן'].items():
            f.write(f"  {pkid}: {data['מקום']} - {data['משל']}\n")
        f.write("\n")

        # ניגודים
        f.write("ניגודים:\n")
