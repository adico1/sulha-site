# הלוך ורצוא - רצוא ושוב
# =============================================================================

def ratzo_vashov(ain_count, yesh_state):
    """
    הלוך ורצוא - מאין ליש (התרחבות)
    רצוא ושוב - מיש לאין (התכווצות)
    """
    # התרחבות: אין → יש
    expansion = {
        'מאין': ain_count,
        'ליש': yesh_state,
        'כיוון': 'פנימה',
        'פעולה': 'התגבשות'
    }

    # התכווצות: יש → אין (חזרה לשורש)
    contraction = {
        'מיש': yesh_state,
        'לאין': ain_count,
        'כיוון': 'החוצה',
        'פעולה': 'צמצום'
    }

    return expansion, contraction


# =============================================================================
# כתיבת שדה מורחב
# =============================================================================

def write_sade():
    """כתוב שדה - יש מאין מלא"""
    YESH_DIR.mkdir(exist_ok=True)

    # אסוף נתונים
    sefer = sefer_text()
    sfar = sfar_number()
    sipur = sipur_story()

    # ספור פתוחים
    patuach = 0
    naul = 0
    for ai_file in AIN_DIR.glob("*.אי"):
        with open(ai_file, 'r', encoding='utf-8') as f:
            content = f.read()
            if 'סור: פתוח' in content:
                patuach += 1
            elif 'סור: נעול' in content:
                naul += 1

    sade_path = YESH_DIR / "שדה"
    with open(sade_path, 'w', encoding='utf-8') as f:
        f.write("# שדה - יש מאין\n\n")

        # מי
        f.write("מי:\n")
        f.write(f"  שם: {MI['שם']}\n")
        f.write(f"  תארים: {' '.join(MI['תארים'])}\n")
        f.write(f"  מקום: {MI['מקום']}\n")
        f.write(f"  מעלה: {MI['מעלה']}\n")
        f.write(f"  קדושה: {MI['קדושה']}\n\n")

        # שלשה ספרים
        f.write("שלשה_ספרים:\n")
        f.write("  ספר_כתב: תלי (עולם/מבנה)\n")
        f.write("  ספר_מספר: גלגל (שנה/סדר)\n")
        f.write("  ספור_סיפור: לב (נפש/מהות)\n\n")

        # אין
        kak = sfar['ק"ק']
        f.write("אין:\n")
        f.write(f"  ק\"ק: {kak} (100×100)\n")
        f.write(f"  פתוח: {patuach}\n")
        f.write(f"  נעול: {naul}\n\n")

        # ספר - מבנה
        f.write("ספר_כתב:\n")
        for key, count in sorted(sefer.items(), key=lambda x: -x[1]):
            f.write(f"  {key}: {count}\n")
        f.write("\n")

        # ספר - מספר
        f.write("ספר_מספר:\n")
        for key, val in sfar.items():
            f.write(f"  {key}: {val}\n")
        f.write("\n")

        # ספור - סיפור
        f.write("ספור_סיפור:\n")
        for key, val in sipur.items():
            f.write(f"  {key}: {val}\n")
        f.write("\n")

        # הלוך ורצוא
        f.write("הלוך_ורצוא:\n")
        f.write("  רצוא: אין → יש (התרחבות פנימית)\n")
        f.write("  שוב: יש → אין (התכווצות לשורש)\n")
        f.write("  מחזור: אלהים חיים\n\n")

        # שמות
        f.write("שמות:\n")
        f.write("  יה: גרעין\n")
        f.write("  יהוה: התרחבות\n")
        f.write("  צבאות: hosts (אין)\n")
        f.write("  אלהי_ישראל: כיוון\n")
        f.write("  אלהים_חיים: לולאה\n")
        f.write("  מלך_עולם: שלמות\n\n")

        # ברית
        f.write("ברית_אברהם:\n")
        f.write("  עשר_רגליים: ברית המילה\n")
        f.write("  עשר_ידיים: הלשון\n")
        f.write("  עשרים_ושתים: אותיות בלשון\n")
        f.write("  יסודן: משכן במים דלקם באש רעשן ברוח\n")
        f.write("  נהגם: בשבעה (כפולות)\n")
        f.write("  מזלות: בשנים עשר (פשוטות)\n")

    return sade_path


