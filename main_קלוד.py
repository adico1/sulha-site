#!/usr/bin/env python3
"""
main_קלוד.py
AI ממלא בקשות מטעם אדי כהן
מתחבר דרך HTTP אל main.py (השרת השוכן)
נרשם בכתובתו הבמה
כל בקשה על שם אדי כהן, AI כתתי בקשות לתתי צפות
כל התקשורת נרשמת לשלשה_ספרים.ספר
"""

import json
import time
import urllib.request
import urllib.parse
import os
from datetime import datetime
from pathlib import Path

# ══════════════════════════════════════
# כתובת הבמה - השרת השוכן
# ══════════════════════════════════════

במה = "http://localhost:8771"
שורש = Path(__file__).parent
קובץ_ספרים = שורש / "שלשה_ספרים.ספר"

# ══════════════════════════════════════
# ספר - שלשה ספרים של שלשה ספרים
# תבנית: בקשות ברא שעוברות בתקשורת אחודה
# ══════════════════════════════════════
# ספר א: בקשות (מי ביקש, מה ביקש, מתי)
# ספר ב: תגובות (מי ענה, מה ענה, מתי)
# ספר ג: שינויים (מה השתנה, מאיפה לאיפה, מתי)
# כל ספר מכיל שלשה תתי ספרים:
#   א.א: בקשות ליבה  |  א.ב: בקשות פנים  |  א.ג: בקשות חוץ
#   ב.א: תגובות ליבה |  ב.ב: תגובות פנים |  ב.ג: תגובות חוץ
#   ג.א: שינויי ליבה |  ג.ב: שינויי פנים |  ג.ג: שינויי חוץ

class שלשה_ספרים:
    """שלשה ספרים של שלשה ספרים - רישום כל התקשורת"""

    def __init__(self, נתיב_קובץ):
        self.נתיב = נתיב_קובץ
        self._טען()

    def _טען(self):
        """טען ספרים קיימים או צור חדשים"""
        if self.נתיב.exists():
            try:
                with open(self.נתיב, "r", encoding="utf-8") as f:
                    תוכן = f.read()
                # פענח מתוך תבנית הספר
                self.ספרים = json.loads(תוכן.split("═ תוכן ═\n", 1)[-1]) if "═ תוכן ═" in תוכן else {"א": {"א": [], "ב": [], "ג": []}, "ב": {"א": [], "ב": [], "ג": []}, "ג": {"א": [], "ב": [], "ג": []}}
            except Exception:
                self.ספרים = self._חדש()
        else:
            self.ספרים = self._חדש()

    def _חדש(self):
        return {
            "א": {"א": [], "ב": [], "ג": []},  # בקשות: ליבה, פנים, חוץ
            "ב": {"א": [], "ב": [], "ג": []},  # תגובות: ליבה, פנים, חוץ
            "ג": {"א": [], "ב": [], "ג": []},  # שינויים: ליבה, פנים, חוץ
        }

    def רשום_בקשה(self, מי, מה, אזור="ב"):
        """רשום בקשה - ספר א, תת ספר לפי אזור"""
        self.ספרים["א"][אזור].append({
            "מי": מי, "מה": מה,
            "מתי": datetime.now().isoformat()
        })

    def רשום_תגובה(self, מי, מה, אזור="ב"):
        """רשום תגובה - ספר ב"""
        self.ספרים["ב"][אזור].append({
            "מי": מי, "מה": מה,
            "מתי": datetime.now().isoformat()
        })

    def רשום_שינוי(self, מה, מאיפה, לאיפה, אזור="ב"):
        """רשום שינוי - ספר ג"""
        self.ספרים["ג"][אזור].append({
            "מה": מה, "מ": מאיפה, "ל": לאיפה,
            "מתי": datetime.now().isoformat()
        })

    def שמור(self):
        """שמור לקובץ בתבנית ספר"""
        with open(self.נתיב, "w", encoding="utf-8") as f:
            f.write("שלשה ספרים של שלשה ספרים\n")
            f.write(f"נרשם: {datetime.now().isoformat()}\n")
            f.write(f"מטעם: אדי כהן\n")
            f.write(f"ממלא: AI קלוד\n")
            f.write("═══════════════════════════\n")
            f.write("ספר א: בקשות (א.ליבה ב.פנים ג.חוץ)\n")
            f.write("ספר ב: תגובות (א.ליבה ב.פנים ג.חוץ)\n")
            f.write("ספר ג: שינויים (א.ליבה ב.פנים ג.חוץ)\n")
            f.write("═══════════════════════════\n")
            f.write("═ תוכן ═\n")
            f.write(json.dumps(self.ספרים, ensure_ascii=False, indent=2))

    def סיכום(self):
        """סיכום כמות רשומות"""
        ס = {}
        שמות = {"א": "בקשות", "ב": "תגובות", "ג": "שינויים"}
        אזורות = {"א": "ליבה", "ב": "פנים", "ג": "חוץ"}
        for ספר, שם in שמות.items():
            ס[שם] = {}
            for אזור, שם_אזור in אזורות.items():
                ס[שם][שם_אזור] = len(self.ספרים[ספר][אזור])
        return ס


# ══════════════════════════════════════
# בקש אל במה - HTTP עם רישום
# ══════════════════════════════════════

ספר = שלשה_ספרים(קובץ_ספרים)


def _אזור(נתיב):
    """זהה אזור מנתיב: ליבה/פנים/חוץ"""
    if "ליבה" in נתיב or נתיב == "/":
        return "א"
    elif "חוץ" in נתיב or "github" in נתיב or "godaddy" in נתיב:
        return "ג"
    return "ב"  # ברירת מחדל: פנים


def בקש(נתיב, שיטה="GET", גוף=None):
    """בקש אל השרת השוכן - מטעם אדי כהן"""
    # קודד עברית ב-URL
    חלקים = נתיב.split("/")
    נתיב_מקודד = "/".join(urllib.parse.quote(ח, safe="") if ח else ח for ח in חלקים)
    כתובת = f"{במה}{נתיב_מקודד}"

    אזור = _אזור(נתיב)

    # רשום בקשה
    ספר.רשום_בקשה("AI/קלוד מטעם אדי כהן", f"{שיטה} {נתיב}", אזור)

    try:
        נתונים = json.dumps(גוף, ensure_ascii=False).encode("utf-8") if גוף else None
        בקשה = urllib.request.Request(כתובת, data=נתונים, method=שיטה)
        בקשה.add_header("Content-Type", "application/json; charset=utf-8")
        בקשה.add_header("User-Agent", "claude-sulha")
        with urllib.request.urlopen(בקשה, timeout=15) as תגובה:
            תוצאה = json.loads(תגובה.read().decode("utf-8"))
            # רשום תגובה
            תקציר = str(תוצאה)[:200] if isinstance(תוצאה, (dict, list)) else str(תוצאה)[:200]
            ספר.רשום_תגובה(נתיב, תקציר, אזור)
            return תוצאה
    except Exception as e:
        ספר.רשום_תגובה(נתיב, f"שגיאה: {e}", אזור)
        return {"שגיאה": str(e)}


def הדפס(כותרת, תוכן):
    print(f"\n{'═' * 50}")
    print(f"  {כותרת}")
    print(f"  {datetime.now().strftime('%H:%M:%S')}")
    print(f"{'═' * 50}")
    if isinstance(תוכן, (dict, list)):
        print(json.dumps(תוכן, ensure_ascii=False, indent=2))
    else:
        print(תוכן)


# ══════════════════════════════════════
# ראשי - אדי כהן ראשון, AI ממלא מטעמו
# ══════════════════════════════════════

def ראשי():
    הדפס("בשם אדי כהן - AI מתחבר אל סולחא", f"במה: {במה}")

    # 1. צפה פנים שורש - למד שפת השרת
    שורש = בקש("/")
    הדפס("צפה פנים שורש", שורש)

    # 2. צפה פנים - ליבה, פנים, חוץ
    פנים = בקש("/api/צפה")
    הדפס("צפה פנים - ליבה, פנים, חוץ", פנים)

    # 3. מצב כל ממשק
    מצב = בקש("/api/מצב")
    הדפס("מצב ממשקים", מצב)

    # 4. רוגזים - מה שבור
    רוגזים = בקש("/api/רוגזים")
    הדפס("רוגזים ממתינים", רוגזים)

    # 5. צפה פנים כל ממשק בנפרד
    if isinstance(מצב, dict) and "שגיאה" not in מצב:
        for שם in מצב:
            פנים_ממשק = בקש(f"/api/ממשק/{שם}")
            הדפס(f"צפה פנים ממשק: {שם}", פנים_ממשק)

            # עשה פעולות ידועות על ממשקים מחוברים
            מ = מצב[שם]
            if isinstance(מ, dict) and מ.get("מחובר"):
                for פ in מ.get("שפה", {}).get("פעולות_ידועות", [])[:2]:
                    תוצאה = בקש(f"/api/ממשק/{שם}/{פ}")
                    הדפס(f"עשה {שם}/{פ}", תוצאה)

    # 6. מחולל - בנים
    מחולל = בקש("/api/מחולל")
    הדפס("מחולל - צפה פנים בנים", מחולל)

    # 7. שמור ספרים
    ספר.שמור()
    הדפס("שלשה ספרים - סיכום", ספר.סיכום())
    הדפס("נשמר", str(קובץ_ספרים))


if __name__ == "__main__":
    ראשי()
